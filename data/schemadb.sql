CREATE TABLE CATEGORIAS (
	ID           INTEGER PRIMARY KEY,
	IDPARENT     INTEGER,
	DESCRIPCION  TEXT NOT NULL
	,FOREIGN KEY(IDPARENT) REFERENCES CATEGORIAS(ID)
);

CREATE TABLE APUNTES (
	ID             INTEGER PRIMARY KEY AUTOINCREMENT,
	FH             TEXT, 
	IDCATEGORIA    INT,
	IDSUBCATEGORIA INT,
	IMPORTE        REAL,
	USER	       TEXT,
	DETALLE	       TEXT,
	FOREIGN KEY(IDCATEGORIA) REFERENCES CATEGORIAS(ID),
	FOREIGN KEY(IDSUBCATEGORIA) REFERENCES CATEGORIAS(ID)
);

